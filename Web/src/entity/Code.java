package entity;

public class Code {
	private Interger codeNo;
	private String codeName;
	public Code() {

	}
	public Interger getCodeNo() {
		return codeNo;
	}
	public void setCodeNo(Interger codeNo) {
		this.codeNo = codeNo;
	}
	public String getCodeName() {
		return codeName;
	}
	public void setCodeName(String codeName) {
		this.codeName = codeName;
	}

}


