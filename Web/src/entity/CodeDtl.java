package entity;

public class CodeDtl {
	private Interger codeNo;
	private Interger codeDtlNo;
	private String codeDtlName;

	public CodeDtl() {

	}

	public Interger getCodeNo() {
		return codeNo;
	}

	public void setCodeNo(Interger codeNo) {
		this.codeNo = codeNo;
	}

	public Interger getCodeDtlNo() {
		return codeDtlNo;
	}

	public void setCodeDtlNo(Interger codeDtlNo) {
		this.codeDtlNo = codeDtlNo;
	}

	public String getCodeDtlName() {
		return codeDtlName;
	}

	public void setCodeDtlName(String codeDtlName) {
		this.codeDtlName = codeDtlName;
	}

}
